export function getCourseById(id) {
  return coursesData.find((c) => String(c.id) === String(id)) || null;
}

export function getGradient(category) {
  const g = CATEGORY_GRADIENTS[category] || ["#2563eb", "#7c3aed"];
  return `linear-gradient(135deg, ${g[0]}, ${g[1]})`;
}

export function getInitials(title) {
  if (!title) return "??";
  return title
    .split(" ")
    .slice(0, 2)
    .map((w) => w[0])
    .join("")
    .toUpperCase();
}

// Total duration in minutes from "Xh Ym" string
export function parseDuration(str) {
  if (!str) return 0;
  const h = str.match(/(\d+)h/);
  const m = str.match(/(\d+)m/);
  return (h ? parseInt(h[1]) * 60 : 0) + (m ? parseInt(m[1]) : 0);
}
